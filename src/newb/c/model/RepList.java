package newb.c.model;

public class RepList {
	
	private String result;
	
	private int count;

	public RepList(String result, int count) {
		super();
		this.result = result;
		this.count = count;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	
}
